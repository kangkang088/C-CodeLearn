#define _CRT_SECURE_NO_WARNINGS 1
#include<stdio.h>

//整数的二进制表示有三种表示形式：
//1.正整数：原反补相同。
//2.负整数：需要计算
//原码：直接通过正负的形式写出的二进制序列
//反码：原码符号位不变，其他位取反
//补码：反码加1

int main(void)
{
    int a = 20;
    // 20
    //原码 00000000 00000000 00000000 00010100  反码补码相同  十六进制：00 00 00 14
    
    int b = 0xfffffff6;
    // -10
    //原码 10000000 00000000 00000000 00001010 十六进制：80 00 00 02
    //反码 11111111 11111111 11111111 11110101 十六进制：ff ff ff f5
    //补码 11111111 11111111 11111111 11110110 十六进制：ff ff ff f6

    //-5   10000000 00000000 00000000 00000101
    //     11111111 11111111 11111111 11111010
    //     11111111 11111111 11111111 11111011

    //3    00000000 00000000 00000000 00000011

    //-5+3 11111111 11111111 11111111 11111110 -2的补码
    //怎么就是-2的补码？求原码可以证明
    //补码     11111111 11111111 11111111 11111101
    //原码     10000000 00000000 00000000 00000010 -2

    //-1   1000 0001 - 1111 1110 - 1111 1111
    //-127 1111 1111 - 1000 0000 - 1000 0001
    //-128                       - 1000 0000

    printf("%d\n", b);
    return 0;
}